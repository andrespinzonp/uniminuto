    private void Btn_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_CalcularActionPerformed
        DecimalFormat df = new DecimalFormat("#.###############");
        int n = Integer.parseInt(N_jtf.getText());
        double x, y;
        BigDecimal gral = new BigDecimal(Grados_jtf.getText());
        double r = Double.parseDouble(r_jtf.getText());
        BigDecimal pireal = new BigDecimal(Math.PI * Math.pow(r, 2));
        double dif = 0.0;
        int puntos_in = 0;
        int aux = Integer.parseInt(N_jtf.getText());
        BigDecimal bd1 = new BigDecimal(n);
        BigDecimal piest = new BigDecimal(0);
        
        Pireal_jtf.setText(df.format(pireal).toString());
        for (int i = 0; i < aux; i++) {
            x = Math.random();
            y = Math.random();
            if (calcuarDistancia(x, y) <= r) {
                puntos_in++;
            }
        }
        Puntosin_jtf.setText(Integer.toString(puntos_in));
        BigDecimal bd2 = new BigDecimal(puntos_in);
        BigDecimal bd3 = new BigDecimal(4);
        
        piest = bd2.divide(bd1);
        piest = piest.multiply(bd3);
        Piesti_jtf.setText(piest.toString());
        Dif_jtf.setText(df.format(piest.subtract(pireal).abs()));
        
        int comp;
        comp = (piest.subtract(pireal).abs()).compareTo(gral);
        if (comp <= 0) {
            JOptionPane.showMessageDialog(null, "Bingo!");
        }
    }//GEN-LAST:event_Btn_CalcularActionPerformed

