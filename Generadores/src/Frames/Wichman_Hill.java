    public double congruencialLineal(double semilla, int ite, int a, int b, int m) {
        double resu, u = 0;
        DecimalFormat df = new DecimalFormat("#.##");
        for (int i = 0; i < ite; i++) {
            System.out.print((i + 1) + " - Semilla=" + df.format(semilla));
            resu = ((a * semilla) + b) % m;
            semilla = resu;
            u = semilla / (m - 1);
            System.out.println("     X=" + resu + "      U=" + u);
        }
        return u;
    }

