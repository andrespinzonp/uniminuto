    private void Generar_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Generar_btnActionPerformed
        Wichman_Hill wh = new Wichman_Hill();
        int ite = Integer.parseInt(Itera_tf.getText());
        DefaultListModel listax = new DefaultListModel();
        DefaultListModel listay = new DefaultListModel();
        DefaultListModel listaz = new DefaultListModel();
        DefaultListModel listau = new DefaultListModel();
        double xo = Double.parseDouble(Xo_tf.getText());
        double yo = Double.parseDouble(Yo_tf.getText());
        double zo = Double.parseDouble(Zo_tf.getText());
        double resu;

        for (int i = 0; i < ite; i++) {
            double xi = wh.congruencialLineal(xo, 1, 171, 0, 30269);
            listax.addElement(xi);
            xo = xi;
            double yi = wh.congruencialLineal(yo, 1, 172, 0, 30307);
            listay.addElement(yi);
            yo = yi;
            double zi = wh.congruencialLineal(zo, 1, 170, 0, 30323);
            listaz.addElement(zi);
            zo = zi;
            double u = (xi / 30269) + (yi / 30307) + (zi / 30323);
            listau.addElement(u);
        }
        ListaX.setModel(listax);
        ListaY.setModel(listay);
        ListaZ.setModel(listaz);
        ListaU.setModel(listau);

    }//GEN-LAST:event_Generar_btnActionPerformed

